            <section id="spirale-reasons" class="spirale-section feature-list_2">
                <div class="spirale-section__content feature-list_2">
                    <h2 class="spirale-heading">Notre leitmotiv</h2>
                    <ul class="feature-list feature-list_beautiful">
                        <li class="feature-list_beautiful-item  spirale-shadow--2dp">
                            <span>Produire du message et accélérer sa diffusion </span>
                            <p>
                                Spirale possède une grande connaissance des médias en Afrique et en Europe. 
                                Forte de son expérience et de son réseau, Spirale active, dans les plus brefs délais, 
                                un plan média, de rédiger et de diffuser un communiqué de presse, d’organiser 
                                une conférence de presse, de superviser un reportage de télévision, de calibrer
                                une interview…Par la rédaction de tribunes de haut-niveau et leur publication
                                dans des journaux de référence, la livraison régulière de notes thématiques sur
                                un sujet d’actualité, l’accompagnement dans l’élaboration d’un plaidoyer pertinent,
                                Spirale possède une riche expertise africaine qui permet à ses clients d’être visibles,
                                crédibles et actifs.
                            </p>
                        </li>
                       <li class="feature-list_beautiful-item  spirale-shadow--2dp">
                            <span>Assurer l’éloquence du propos</span>
                            <p>
                                Une communication efficace repose sur la pertinence du message mais aussi sur la qualité 
                                de sa diffusion. À chaque public et à chaque cible correspondent des règles, 
                                des rythmes et des codes médiatiques. En fonction des besoins du client,
                                Spirale définit et explicite les différents formats d’expression médiatique par l’offre
                                du sur-mesure ou des conseils techniques, des entrainements spécifiques à la prise de parole,
                                l’élaboration des éléments de langage, etc. 
                            </p>
                        </li>
                        <li class="feature-list_beautiful-item  spirale-shadow--2dp">
                            <span>Orienter le débat avec du contenu et de la présence</span>
                            <p>
                                Pour faire valoir leurs arguments et leurs intérêts aux niveaux national et international,
                                les décideurs ont l’obligation de participer au débat public, d’influencer les termes 
                                de ce débat et de corriger une perception trop souvent défavorable. Spirale a les armes 
                                pour aider à assumer cette question de leadership sur toutes les surfaces médiatiques
                                (presses écrite, audiovisuelle, Internet) et non médiatiques.
                            </p>
                        </li>
                        <li class="feature-list_beautiful-item  spirale-shadow--2dp">
                            <span>Nourrir la Toile et la dominer</span>
                            <p>
                                Avec le boom de la téléphonie mobile et la généralisation du haut-débit,
                                les réseaux sociaux et les sites d’actualité sur internet sont devenus 
                                en Afrique des acteurs de premier plan dans la diffusion de l’information
                                et l’évolution des opinions publiques. Ce sont des agents redoutables d’influence
                                capables de construire et de déconstruire une réputation.
                                Par sa maîtrise de la communication digitale, Spirale corrige et améliore 
                                la e-réputation de ses clients.
                            </p>
                        </li>
                        <li class="feature-list_beautiful-item  spirale-shadow--2dp">
                            <span>Mettre la passerelle entre les décideurs</span>
                            <p>
                                En Europe, les experts et les décideurs capables de structurer et de promouvoir
                                un argumentaire sur l’Afrique sont encore rares ou de qualité perfectible.
                                Pour identifier les véritables cercles de décision qui peuvent faire la différence,
                                Spirale a mis en place un partenariat avec des agences spécialisées 
                                dans la stratégie institutionnelle à Bruxelles, Londres et à Paris.
                                Par son réseau africain parmi les décideurs économiques et institutionnels,
                                Spirale facilite l’accès à des marchés spécifiques et aide à identifier les bons 
                                interlocuteurs et à évaluer le risque-pays ou Country Profil. 
                                L’enjeu est de tisser des liens de confiance dans la durée.
                            </p>
                        </li>
                    </ul>
